<h3>Prospect</h3>
<p class="smaller">A prospect is a user which has not yet been validated. In order to be validated a prospect will have to wait to see if the
clan has a need for you. A prospect may be removed if the clan knows that they will not need you.</p>
<p class="smaller">It's only administrators of this website that are able to promote you from prospect to user. As a valid user you will be able
to comment news.</p>